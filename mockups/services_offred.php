       <p>Extensive system review, material recommendations and design assistance (followed by proper installation) ensure that each home operates as efficiently and effectively as possible. </p>
       <h1>INDOOR AIR QUALITY AUDITS</h1>
       <p>Gord Cooke and his team were among the first in Canada to complete the Canada Mortgage and Housing Indoor Air Quality Auditors training and apprenticeship program. 
</p>
       <p>This in-home investigation service, offered to homeowners in Southern Ontario, is a comprehensive investigation with a full, written report identifying opportunities for improving air quality and ensuring homeowners are breathing the best possible air in the home. The process focuses on identifying solutions that are practical and affordable. Audit services are also available for small commercial buildings so employees can enjoy a healthier environment. </p>
       <h1>SPECIALIZED AIR QUALITY TESTING</h1>
       <p>Some air quality problems are the result of complex and often hidden problems. In these cases, Gord Cooke offers, in cooperation with certified laboratories : 
</p>
       <ul>
         <li>Mold Testing           </li>
         <li>Volatile Organic Compound Testing           </li>
         <li>Combustion Products Analysis           </li>
         <li>Particulate Matter Identification           </li>
         <li>Material Testing </li></ul>
         <h1>AIR TIGHTNESS TESTING </h1>
         <p>Unwanted air leakage in and out of houses and buildings is responsible for as much as 40% of the heating bill for most homeowners. In addition, many comfort problems, building failures and mold concerns are related to air leakage. Gord Cooke has both the specialized equipment and expertise to test and solve air leakage problems in houses, duct systems and small commercial buildings in Southern Ontario. </p>
         <h1>ENERGY AUDITS AND SIMULATIONS </h1>
         <p>Gord Cooke has 15 years of experience in performance testing, energy auditing and energy simulation of houses and small commercial buildings. He is a certified R-2000 design evaluator, air tightness tester and inspector, an EnerGuide for Houses Quality Assurance contractor and an ENERGY STAR contractor. He can help builders, renovators and homeowners optimize energy use in new and existing homes. 
</p>
