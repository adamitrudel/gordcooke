      <h1><img src="images/inner_photos/success-stories.jpg" width="169" height="312" align="right" />SERVICES OFFERED</h1>
      <p>Gord Cooke offers comprehensive consulting services to help builders, architects and remodelers continuously improve their practices to deliver high performance homes. Gord provides support from the smallest first steps to a complete change in design and construction practices. He helps you fix problems as they arise and, most importantly, before they happen.
</p>
      <p>He has helped hundreds of clients achieve the goals of building programs such as LEED for Homes, ENERGY STAR for New Homes, American Lung Association’s Health House Program, and others. </p>
      <h1>SITE ASSESSMENTS AND WORKSHOPS</h1>
      <p>As an essential complement to his training services, Gord Cooke offers interactive, on-site workshops focused on identifying key design and construction issues and creating solutions. These workshops are critical to building better homes as they provide the opportunity to bring together diverse stakeholders (site supervisors, sales staff, construction trades, etc.) to discuss key issues and challenges, and provide a forum for identifying and solving problems.
</p>
      <p>Workshops combine training seminars with construction site walk-throughs. A follow-up report outlines observations made on site, including problem areas, best practices, and a planning timeline for implementing changes. </p>
      <h1>PERFORMANCE EVALUATION & RECOMMENDATIONS</h1>
      <p><strong>Assessments
      </strong></p>
      <p>Design and construction assessments are one of the most important beginning steps to building better homes. Gord Cooke provides expert design and construction reviews to assist his clients in developing a clear picture of current practices, short and long term goals, and a roadmap to achieve these goals.        </p>
      <p><strong>Recommendations</strong></p>
      <p>A home is not complete unless Gord Cooke and his team ensure that it’s performing to the standard to which it was designed. Gord provides coordination of testing and diagnostic services and recommendations in order to provide an essential feedback loop on the end performance of each home. His role is to help you develop a good test plan, make sure it’s being implemented appropriately and help you understand and act on test results. </p>
      <h1>PLAN & SPECIFICATIONS REVIEW</h1>
      <p>An average home consists of 15,000 building components that are put together by over 200 different people from 14 different trades. But a home is much more than an assembly of parts. A home is an interactive system and each of its components must be designed to work together. During a plan review, Gord analyzes the following components with the “Systems Approach” in mind :</p>
      <ul>
        <li> 	Foundations</li>
