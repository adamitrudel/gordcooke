  <h1><img src="images/inner_photos/success-stories.jpg" width="169" height="312" align="right" />BIOGRAPHY</h1>
      <p> Welcome to my website that is dedicated to helping the building industry and trade contractors build, renovate and sell more efficient, comfortable, healthier and more durable homes. As a professional engineer, trainer, author and industry consultant for the last 25 years, I have had the pleasure of working with great building professionals across North America who are dedicated to a path of continual improvement. 
</p>
      <p>As in all the training I do, I welcome your comments, feedback and interaction. We are a large and diverse industry – manufacturers, designers, consultants, suppliers, trade contractors, sales agents, building officials, energy raters, government representatives, builders and renovators. All of us have an important viewpoint on the industry, so please engage in the betterment of our industry by using and responding to the information presented.
      </p>
      <p>Thank you: Gord Cooke
      </p>
      <p><strong>Biography
      </strong></p>
      <p>For the past two decades, Gord Cooke has been an effective and passionate educator and advocate for better building practices, improved indoor air quality and energy efficiency. He has a unique talent of taking the complex issues of building science and breaking them into easily understood elements with practical real-world applications for all audiences.
      Gord’s building science training curricula covers the gamut from creating and delivering basic and advanced building science principles through managing indoor air quality to HVAC system design to an intensive sales training workshop. He has pioneered new training programs designed to assist builders in the sales and marketing of higher performing, energy efficient homes -- helping builders more effectively tell their story to customers, increase their margins and sell more homes.</p>
      <p> Gord has delivered sessions at such conferences as the NAHB’s International Builders Show, Energy Design Conference, JLC Live, West Coast Green and the National Green Building Conference and is an active Energy & Environmental Building Association (EEBA) board member and certified trainer. He authored the Build Comfort Program and provided instruction for both the Union and Enbridge Gas companies. Additionally, Gord has authored and co-authored numerous white papers and research studies related to indoor air quality and home performance issues, contributes a monthly IAQ column to HPAC magazine and is a certified R-2000 Design Evaluator and Trainer.
      </p>
